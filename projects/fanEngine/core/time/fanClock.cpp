#include "core/time/fanClock.hpp"

namespace fan
{
    //==================================================================================================================================================================================================
    //==================================================================================================================================================================================================
    Clock::Clock()
    {
        mStartPoint = mClock.now();
    }

    //==================================================================================================================================================================================================
    //==================================================================================================================================================================================================
    Clock::TimePoint Clock::Now() const
    {
        return TimePoint( mClock.now() );
    }

    //==================================================================================================================================================================================================
    //==================================================================================================================================================================================================
    void Clock::Reset()
    {
        mClock      = std::chrono::high_resolution_clock();
        mStartPoint = mClock.now();
    }

    //==================================================================================================================================================================================================
    //==================================================================================================================================================================================================
    float Clock::ElapsedSeconds() const
    {
        return SecondsBetween( mStartPoint, Now() );
    }
}